package adt

// Each of these problem is a simple exercise using algebraic data types. This
// make good exercises to get used to the syntax.
object FingerExercises {
  // A Continent is
  // - Asia;
  // - Africa;
  // - North America;
  // - South America;
  // - Antartica;
  // - Europe; or
  // - Australia
  //
  // Represent this in code
  //
  // Implement a method that returns the surface area of a continent in square kilometres.
  // The areas are:
  // - Asia 44,614,000
  // - Africa 30,365,000
  // - North America 24,230,000
  // - South America 17,814,000
  // - Antarctica 14,200,000
  // - Europe 10,000,000
  // - Australia 8,510,90

  // A Hotel booking is
  // - a Hotel Name (String);
  // - an Address (String);
  // - a Check-in Date (java.time.LocalDate); and
  // - a Check-out Date (java.time.LocalDate)
  //
  // Represent this in code

  // A Canis, the genus that includes dogs, is one of seven species:
  // - Side-striped Jackal;
  // - Golden Jackal;
  // - Coyote;
  // - Gray Wolf;
  // - Black-backed Jackal;
  // - Red Wolf; or
  // - Ethiopian Wolf
  //
  // Represent this in code

  // A BlogPost is
  // - a Title (String);
  // - a Body (String);
  // - an Author (String); and
  // - a State (see below)
  //
  // A State is
  // - Draft; or
  // - Published, which has a publication data (java.time.LocalDateTime)
  //
  // Represent this in code
  //
  // Implement a method to render a blog post. You should output a string in
  // Markdown format and clearly indicate if the state of the blog post.
  // Otherwise the choice of the formatting is up to you.
  //
  // Protip: using string interpolation will make this task easier.
}
